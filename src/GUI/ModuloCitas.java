    private void btnCitaAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCitaAgregarActionPerformed
        // TODO add your handling code here:
        int vPlaca = Integer.parseInt(txtCitaAgregarVehiculoPlaca.getText());
        
        String vVehiculo = PantallaPrincipal.Gestora.BuscarVehiculoPorPlaca(vPlaca);
        
        int IndexLugar = cmbCitaAgregarLugar.getSelectedIndex();
        String vLugar = (String)cmbCitaAgregarLugar.getItemAt(IndexLugar);
        
        int vCosto =  Integer.parseInt(txtCitaAgregarCosto.getText());
        
        JOptionPane.showMessageDialog(null, PantallaPrincipal.Gestora.AgregarCita(vVehiculo, vLugar, vCosto));

    }//GEN-LAST:event_btnCitaAgregarActionPerformed

