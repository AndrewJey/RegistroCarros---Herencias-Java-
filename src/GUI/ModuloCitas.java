    private void btnAbrirCitasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirCitasActionPerformed
        // TODO add your handling code here:
        int Resultado = Archivo.showOpenDialog(this);
        
        if (Resultado == (JFileChooser.CANCEL_OPTION)){
            return;
        }
        
        File ArchivoPorEnviar = Archivo.getSelectedFile();
        
        JOptionPane.showMessageDialog(null, PantallaPrincipal.Gestora.AbrirCitas(ArchivoPorEnviar));
    }//GEN-LAST:event_btnAbrirCitasActionPerformed

    private void btnGuardarCitasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCitasActionPerformed
        // TODO add your handling code here:
        
        Archivo.setFileSelectionMode(JFileChooser.FILES_ONLY);
        
        int resultado = Archivo.showSaveDialog(this);
        
        if (resultado == JFileChooser.CANCEL_OPTION){
            return;
        }
        
        File ArchivoPorGuardar = Archivo.getSelectedFile();
        
        JOptionPane.showMessageDialog(null, PantallaPrincipal.Gestora.guardarCitas(ArchivoPorGuardar));
        
    }//GEN-LAST:event_btnGuardarCitasActionPerformed

