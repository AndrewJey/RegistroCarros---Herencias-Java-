    private void btnMotocicletaAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMotocicletaAgregarActionPerformed
        // TODO add your handling code here:
        String vMarca = txtMotocicletaAgregarMarca.getText();
        int vPlaca = Integer.parseInt(txtMotocicletaAgregarPlaca.getText());
        double vCilindraje = Double.parseDouble(txtMotocicletaAgregarCilindraje.getText());
        String vColor = txtMotocicletaAgregarColor.getText();

        JOptionPane.showMessageDialog(null, PantallaPrincipal.Gestora.AgregarMotocicleta(vMarca, vPlaca, vCilindraje, vColor));

    }//GEN-LAST:event_btnMotocicletaAgregarActionPerformed

